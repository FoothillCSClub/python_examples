# A tuple is a fixed, immutable collection of objects.
a_tuple = (1, 2, 3, 4)

# a list is a mutable collection of objects.
# a list is most similar to an ArrayList in Java, or Vector in C++
a_list = [1, 2, 3, 4]

# a dict maps keys to stored values
# a dict is similar to a HashMap in java or unordered_map in C++
a_dict = {1: 'Monty', 2: 'Python'}

# a set is an unordered collection of objects, for which the time
# to check for a contained element is O[1]
a_set = {'spam', 'eggs', 'toast'}
