some_int = 5

some_float = 3.54

some_str = 'this is a string'

some_bool = False

none_value = None  # None is the absence of another kind of value.

print(f'int value: {some_int}')
print(f'float value: {some_int}')
print(f'str value: {some_str}')
print(f'bool value: {some_bool}')
print(f'none_value: {none_value}')

